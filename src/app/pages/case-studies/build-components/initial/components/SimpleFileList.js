///////////////////////////////////////////////////////////////
// IMPORTS
///////////////////////////////////////////////////////////////
import React from 'react';

import List from '@material-ui/core/List';

import SimpleFileListItem from './SimpleFileListItem';

///////////////////////////////////////////////////////////////
// COMPONENTS
///////////////////////////////////////////////////////////////
class SimpleFileList extends React.Component {

  ///////////////////////////////////////////////////////////////
  // RENDERS
  ///////////////////////////////////////////////////////////////
  renderItem(item, index) {
    return (
      <SimpleFileListItem key={index} item={item} />
    );
  }

  ///////////////////////////////////////////////////////////////
  // INSTANCE METHODS
  ///////////////////////////////////////////////////////////////
  render() {
    return (
      <List>
        {this.props.data.map((x, i) => this.renderItem(x, i))}
      </List>
    );
  }

}

///////////////////////////////////////////////////////////////
// EXPORTS
///////////////////////////////////////////////////////////////
export default SimpleFileList;
