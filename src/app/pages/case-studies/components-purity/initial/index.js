///////////////////////////////////////////////////////////////
// IMPORTS
///////////////////////////////////////////////////////////////
import React from 'react';

import Grid from '@material-ui/core/Grid';
import Button from '@material-ui/core/Button';

import PrintUpdates from './components/PrintUpdates';

///////////////////////////////////////////////////////////////
// COMPONENTS
///////////////////////////////////////////////////////////////
class CaseStudyInitial extends React.Component {

  ///////////////////////////////////////////////////////////////
  // CONSTRUCTOR
  ///////////////////////////////////////////////////////////////
  constructor(props, context) {
    super(props, context);
    this.state = { number: 0 };
  }

  ///////////////////////////////////////////////////////////////
  // EVENTS
  ///////////////////////////////////////////////////////////////
  onClickButton(e) {
    e.preventDefault();
    this.setState({ number: this.state.number + 1 });
  }

  ///////////////////////////////////////////////////////////////
  // RENDERS
  ///////////////////////////////////////////////////////////////
  render() {
    return (
      <Grid container spacing={3}>
        <Grid item xs={6} sm={6}>
          <Button variant="outlined" color="primary" onClick={(e) => this.onClickButton(e)}>
            Click me
          </Button>
        </Grid>
        <Grid item xs={6} sm={6}>
          Clicks: <b>{this.state.number}</b><br/>
          <PrintUpdates />
        </Grid>
      </Grid>
    );
  }

}

///////////////////////////////////////////////////////////////
// EXPORTS
///////////////////////////////////////////////////////////////
export default CaseStudyInitial;
